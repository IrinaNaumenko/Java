package homework.one_for_11_10_22;

public class OurGrup {

    private String name;
    private int gropNumber;
    private String email;
    private int id;
    private int age;

    public OurGrup() {
        this.name = name;
        this.gropNumber = gropNumber;
        this.email = email;
        this.id = id;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getGropNumber() {
        return gropNumber;
    }

    public String getEmail() {
        return email;
    }

    public int getId() {
        return id;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setGropNumber(int gropNumber) {
        this.gropNumber = gropNumber;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
